def parse_site_payload(raw_payload):
    raw_payload = (raw_payload or '').strip()
    if not raw_payload:
        return None

    try:
        return json.loads(raw_payload)
    except json.JSONDecodeError as exc:
        raise ValueError(f'Некорректный JSON в payload: {exc.msg}') from exc

