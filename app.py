def normalize_block_payload(payload):
    if isinstance(payload, dict):
        return payload
    if isinstance(payload, str):
        raw = payload.strip()
        if not raw:
            return {}
        try:
            parsed = json.loads(raw)
            return parsed if isinstance(parsed, dict) else {}
        except json.JSONDecodeError:
            return {}
    return {}

def sync_current_cms_block_values(page_slug, blocks):
    sync_rules = CURRENT_CMS_BLOCK_SYNC.get(page_slug)
    if not sync_rules:
        return blocks

    updated = False
    for block in blocks:
        block_rules = sync_rules.get(block.block_key)
        if not block_rules:
            continue

        image_rule = block_rules.get('image_url')
        if image_rule:
            current_image_url = block.image_url
            if current_image_url in image_rule.get('replace_if', []):
                block.image_url = image_rule.get('target')
                updated = True
        button_label_rule = block_rules.get('button_label')
        if button_label_rule:
            current_button_label = block.button_label or ''
            if current_button_label in button_label_rule.get('replace_if', []):
                block.button_label = button_label_rule.get('target') or None
                updated = True

        button_url_rule = block_rules.get('button_url')
        if button_url_rule:
            current_button_url = block.button_url or ''
            if current_button_url in button_url_rule.get('replace_if', []):
                block.button_url = button_url_rule.get('target') or None
                updated = True


        body_rule = block_rules.get('body')
        if body_rule:
            current_body = block.body
            if current_body in body_rule.get('replace_if', []):
                block.body = body_rule.get('target')
                updated = True

        payload_rule = block_rules.get('payload')
        if payload_rule:
            normalized_payload = normalize_block_payload(block.payload)
            if normalized_payload in payload_rule.get('replace_if', []):
                block.payload = payload_rule.get('target')
                updated = True

    if updated:
        db.session.commit()

    return blocks

def sync_public_page_seo_defaults(page_slug):
    if page_slug not in CMS_SEO_SYNC_PAGES:
        return

    page = SitePage.query.filter_by(slug=page_slug).first()
    defaults = PUBLIC_PAGE_SEO_DEFAULTS.get(page_slug)
    if not page or not defaults:
        return

    changed = False
    if (page.meta_title or '').strip() != defaults.get('title'):
        page.meta_title = defaults.get('title')
        changed = True
    if (page.meta_description or '').strip() != defaults.get('description'):
        page.meta_description = defaults.get('description')
        changed = True

    if changed:
        db.session.commit()

def get_site_page_with_blocks(page_slug):
    ensure_default_blocks_for_page(page_slug)
    sync_public_page_seo_defaults(page_slug)

    page = SitePage.query.filter_by(slug=page_slug).first()
    if not page:
        return None, {}

    blocks = SiteBlock.query.filter_by(page_id=page.id).order_by(SiteBlock.sort_order.asc(), SiteBlock.id.asc()).all()
    blocks = sync_current_cms_block_values(page_slug, blocks)
    block_map = {}
    for block in blocks:
        if not block.is_enabled:
            continue
        block.payload = normalize_block_payload(block.payload)
        block.cms_block_type = get_cms_block_type(page_slug, block)
        block_map[block.block_key] = block
    return page, block_map

