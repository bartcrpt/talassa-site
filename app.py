def normalize_legal_document_text(text):
    if not text:
        return ''

    normalized = (
        str(text)
        .replace('\r\n', '\n')
        .replace('**', '')
        .replace('\\.', '.')
        .replace('\\-', '-')
        .replace('\\&', '&')
        .replace('\\+', '+')
        .replace('\\(', '(')
        .replace('\\)', ')')
        .replace('\xa0', ' ')
    )
    normalized = re.sub(r'[ \t]+\n', '\n', normalized)
    normalized = re.sub(r'\n{3,}', '\n\n', normalized)
    return normalized.strip()

